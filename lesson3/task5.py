my_list = [1, 2, 3, 4, 5] # создаем список
print(my_list) # выводим список на экран

my_list.append(6) # добавляем элемент в конец списка
print(my_list) # выводим обновленный список

my_list.remove(4) # удаляем элемент из списка
print(my_list) # выводим обновленный список

my_list.insert(2, 7) # вставляем элемент на определенную позицию
print(my_list) # выводим обновленный список
